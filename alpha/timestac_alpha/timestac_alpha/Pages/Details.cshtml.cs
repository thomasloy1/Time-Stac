using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using timestac_alpha.Data;
using timestac_alpha.Models;

namespace timestac_alpha
{
    public class DetailsModel : PageModel
    {
        private readonly timestac_alpha.Data.timestac_alphaContext _context;

        public DetailsModel(timestac_alpha.Data.timestac_alphaContext context)
        {
            _context = context;
        }

        public User User { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            User = await _context.User.FirstOrDefaultAsync(m => m.ID == id);

            if (User == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
